                   BIO.t.FN,BIO.t.FN.siteperc,BIO.t.FN.rangeperc,BIO.t.F,BIO.t.F.siteperc,BIO.t.F.rangeperc)
  
  ggplot.df[nrow(ggplot.df)+1,] <<- BIO.data
  
